import UIKit


extension UIColor {

    static func turquoise() -> UIColor {
        return #colorLiteral(red: 0, green: 0.82681638, blue: 0.7845831513, alpha: 1)
    }
    
    static func idealGrey() -> UIColor {
        return #colorLiteral(red: 0.3019607843, green: 0.3215686275, blue: 0.337254902, alpha: 1)
    }
    
    
    static func idealBlack() -> UIColor {
        return #colorLiteral(red: 0.02745098039, green: 0.03137254902, blue: 0.0431372549, alpha: 1)
    }
    
    static func idealYellow() -> UIColor {
        return #colorLiteral(red: 1, green: 0.6416495442, blue: 0, alpha: 1)
    }
}
